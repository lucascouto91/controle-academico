    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        DiciplinaBO bo = new DiciplinaBO();
        try {
            Diciplina entidade = criarEntidade();
            bo.consultar(entidade);
            popularFormulario(entidade);
            
        }catch (NegocioException e){
            if(e.getCause()==null){
                JOptionPane.showMessageDialog(this,e.getMessage(),"Mensagem",
                    JOptionPane.WARNING_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this,e.getMessage(),"Mensagem",
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        DiciplinaBO bo = new DiciplinaBO();
        try {
            Diciplina entidade  = criarEntidade();
            bo.inserir(entidade);
            JOptionPane.showMessageDialog(this, "Diciplina cadastrada com sucesso!",
                    "Mensagem", JOptionPane.INFORMATION_MESSAGE);
            limparFormulario();
        } catch (NegocioException e){
            if(e.getCause()==null){
                JOptionPane.showMessageDialog(this,e.getMessage(),"Mensagem",
                        JOptionPane.WARNING_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this,e.getMessage(),"Mensagem",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonIncluirActionPerformed

