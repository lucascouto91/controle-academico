    private void popularFormulario(Aluno entidade){
        jTextFieldMatricula.setText(Integer.toString(entidade.getValidaMatricula()));
        jTextFieldNome.setText(entidade.getValidaNome());
        jTextFieldCpf.setText(entidade.getValidaCpf());
        jTextFieldCurso.setText(entidade.getValidaCurso());
    }
    
    private void limparFormulario(){
        jTextFieldMatricula.setText("");
        jTextFieldNome.setText("");
        jTextFieldCpf.setText("");
        jTextFieldCurso.setText("");
    }
    
    private Aluno criarEntidade(){
        Aluno entidade = new Aluno();
        if( !"".equals(jTextFieldMatricula.getText())) {
            entidade.setMatricula(Integer.valueOf(jTextFieldMatricula.getText()));
            entidade.setNome(jTextFieldNome.getText());
            entidade.setCpf(jTextFieldCpf.getText());
            entidade.setCurso(jTextFieldCurso.getText());
            
        }else{
            int matricula = 0;
            entidade.setMatricula(matricula);
            entidade.setNome(jTextFieldNome.getText());
            entidade.setCpf(jTextFieldCpf.getText());
            entidade.setCurso(jTextFieldCurso.getText());
            
        }
        return entidade;
    }
    
    
    
    
        

    

