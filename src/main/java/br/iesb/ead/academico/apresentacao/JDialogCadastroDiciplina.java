    private void jTextFieldCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldCodigoKeyTyped
        char TestChar=evt.getKeyChar();
        if(!(Character.isDigit(TestChar)))
            evt.consume();
    }//GEN-LAST:event_jTextFieldCodigoKeyTyped

    private void jTextFieldDiciplinaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldDiciplinaKeyTyped
        char TestChar=evt.getKeyChar();
        if(!(Character.isAlphabetic(TestChar)||Character.isSpaceChar(TestChar)))
            evt.consume();
    }//GEN-LAST:event_jTextFieldDiciplinaKeyTyped

