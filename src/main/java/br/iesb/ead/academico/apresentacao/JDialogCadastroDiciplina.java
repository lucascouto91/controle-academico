    private void popularFormulario(Diciplina entidade){
        jTextFieldCodigo.setText(Integer.toString(entidade.getValidaCodigo()));
        jTextFieldDiciplina.setText(entidade.getValidaDiciplina());
        jTextFieldCargaHoraria.setText(Integer.toString(entidade.getValidaCargaHoraria()));
        
    }
    
    private void limparFormulario(){
        jTextFieldCodigo.setText("");
        jTextFieldDiciplina.setText("");
        jTextFieldCargaHoraria.setText("");
        
    }
    
    private Diciplina criarEntidade(){
        Diciplina entidade = new Diciplina();
        if( !"".equals(jTextFieldCodigo.getText())) {
            if( "".equals(jTextFieldCargaHoraria.getText())){
                int cargaHoraria = 0;
                entidade.setCodigo(Integer.valueOf(jTextFieldCodigo.getText()));
                entidade.setDiciplina(jTextFieldDiciplina.getText());
                entidade.setCargaHoraria(cargaHoraria);
            }else{
                entidade.setCodigo(Integer.valueOf(jTextFieldCodigo.getText()));
                entidade.setDiciplina(jTextFieldDiciplina.getText());
                entidade.setCargaHoraria(Integer.valueOf(jTextFieldCargaHoraria.getText()));
            }
        
        }
        else{
            int codigo = 0;
            int cargaHoraria = 0;
            entidade.setCodigo(codigo);
            entidade.setDiciplina(jTextFieldDiciplina.getText());
            entidade.setCargaHoraria(cargaHoraria);
            
        }
        return entidade;
    }
    
    
    
    

